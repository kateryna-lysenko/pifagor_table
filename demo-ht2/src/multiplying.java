public class multiplying {


    public static void main(String[] args) {

        for (int j=1; j<=10; j++) {

            for (int i = 1; i <= 10; i++) {
                    if (i*j<10) {
                        System.out.print(i * j + "  ");
                    }else System.out.print(i * j + " ");
            }

            System.out.println();

        }

    }
}
